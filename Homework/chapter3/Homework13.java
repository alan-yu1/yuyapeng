public class Homework13 {
    public static void main(String[] args){
        //书名使用字符串保存
        String book1 = "西游记";
        String book2 = "三国演义";
        System.out.println(book1 + book2);
        //性别使用字符保存
        char sex1 = '男';
        char sex2 = '女';
        System.out.println(sex1 + sex2); //得到的是整数（两个字符对应码值相加）
        //价格用小数保存
        float price1 = 0.01F;
        float price2 = 1.00F;
        System.out.println(price1 + price2);
    }
}
