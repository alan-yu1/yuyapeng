public class homework11 {
    public static void main(String[] args){
        int n1;
        n1 = 13;
        int n2;
        n2 = 17;
        int n3;
        n3 = n1 + n2;  //n3 = 17 + 13 = 30
        System.out.println("n3 = " + n3);  //输出30
        int n4 = 38;
        int n5 = n4 - n3;  //n5 = n4 - n3 = 8
        System.out.println("n5 = " + n5);  //输出8
    }
}
